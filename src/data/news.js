// Fonction pour récupérer les dernières actualités (pour la page d'accueil)
export const getLatestNews = (count = 3) => {
  // Tri par date (plus récente d'abord)
  return [...newsData].sort((a, b) => new Date(b.date) - new Date(a.date)).slice(0, count);
};

// Fonction pour récupérer les actualités en vedette
export const getFeaturedNews = () => {
  return newsData.find(news => news.featured);
};

// Fonction pour récupérer une actualité par ID
export const getNewsById = (id) => {
  return newsData.find(news => news.id === parseInt(id));
};

// Obtenir toutes les catégories uniques
export const getAllCategories = () => {
  return [...new Set(newsData.map(news => news.category))];
};
